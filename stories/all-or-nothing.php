<p>
  Have you ever had a crisis in your life in which you deliberately, earnestly,
  and recklessly abandoned everything? It is a crisis of the will. You may come
  to that point many times externally, but it will amount to nothing. The true
  deep crisis of abandonment, or total surrender, is reached internally, not
  externally. The giving up of only external things may actually be an
  indication of your being in total bondage.
</p>
<p>
  Have you deliberately committed your will to Jesus Christ? It is a transaction
  of the will, not of emotion; any positive emotion that results is simply a
  superficial blessing arising out of the transaction. If you focus your
  attention on the emotion, you will never make the transaction. Do not ask God
  what the transaction is to be, but make the determination to surrender your
  will regarding whatever you see, whether it is in the shallow or the deep,
  profound places internally.
</p>
<p>
  If you have heard Jesus Christ’s voice on the waves of the sea, you can let
  your convictions and your consistency take care of themselves by concentrating
  on maintaining your intimate relationship to Him.
</p>
