  <h3>
    <strong>“Enter by the narrow gate Because narrow is the gate and difficult
      is the
      way which leads to life…” —Matthew 7:13-14.</strong>
  </h3>
  <hr>
</div>
<p>
  If we are going to live as disciples of Jesus, we have to remember that all
  efforts of worth and excellence are difficult. The Christian life is
  gloriously difficult, but its difficulty does not appreciate the miraculous
  salvation of jesus Christ enough to be our utmost for His highest—our best for
  His glory?
</p>
<p>
  God saves people by His sovereign grace through the atonement of Jesus, and
  “It is God who works in you both to will and to do for His good pleasure”
  (Philippians 2:13). But we have to “work out” that salvation in our everyday,
  practical living (Philippians 2:12). If we will only sat on the basis of His
  redemption to do what He commands, then we will find that our own nature, as
  well as the grace of God, will stand by us.
</p>
<p>
  Thank God that He does give us difficult things to do! His salvation is a
  joyous thing, but it is also something that requires bravery, courage, and
  holiness. It test us for all we are worth. jesus is “bringing many sons to
  glory” (Hebrews 2:10), and God will not shield us from the requirements of
  sonship. God&apos;s grace produces men and women with a strong family likeness to
  jesus Christ, not pampered, spoiled weaklings. it takes a tremendous amount of
  discipline to live the worthy and excellent life of a disciple of Jesus in the
  realities of life. And it is always necessary for us to make an effort to live
  a life of worth and excellence.
</p>