<p>There is a precious truth for thee, believer. “Thou mayest be poor, or in suffering, or unknown, but for thine
  encouragement take a review of thy "calling" and the consequences that flow from it, and especially that thou are
  God&rsquo;s child today, so surely shall all thy trials soon be at an end, and them shalt be rich to all the intents of
  bliss. Wait awhile, and that weary head shall wear the crown of glory, and that hand of labour shall grasp the
  palm-branch of victory. Lament not thy troubles, but rather rejoice that ere long thou wilt be where “there shall be
  neither sorrow, nor crying, neither shall there be any more pain.” The chariots of fire are at thy door, and a moment
  will suffice to bear thee to the glorified. The everlasting song is almost on thy lip. The portals of heaven stand
  open for thee. Think not that thou canst fail of entering into rest. If He hath called thee, nothing can divide thee
  from His love. Distress cannot sever the bond; the fire of persecution cannot burn the link; the hammer of hell cannot
  break the chain. Thou art secure; that voice which called thee at first, shall call thee yet again from earth to
  heaven, from death&rsquo;s dark gloom to immortality&rsquo;s unuttered splendors. Rest assured, the heart of Him who has justified
  thee beats with infinite love towards thee. Thou shalt soon be with the gloried, where thy portion is; thou art only
  waiting here to be made meet for the inheritance, and that done, the wings of angels shall waft thee far away, to the
  mount of peace, and joy and blessedness, where,</p>
  
<blockquote>
  <p>“Far from a world of grief and sin,<br />
    With God eternally shut in,”</p>
</blockquote>
<p>thou shalt rest for ever and ever.</p>