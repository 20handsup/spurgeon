  <p>
    “Better is the end of thing than the beginning thereof.” —
      Ecclesiastes 7:8
  </p>
  <hr>
</div>
<p>
  Look at David’s Lord and Master; see His beginning. He ws despised and
  rejected of men; a man of sorrows and acquainted with grief. Would you see the
  end? He sits at His Father’s right hand, expecting until His enemies be made
  his footstool. “As He is, so are we also in this world.” You must bear the
  cross, or you shall never wear the crown; you must wade through the mire, or
  you shall never walk the golden pavement. Cheer up, then, poor Christian,
  “Better is the end than the beginning thereof.” See that creeping worm, how
  contemptible its appearance! It is the beginning of a ching. Mark that insect
  with gorgeous wings, playing in the sunbeams, sipping at the flower bells,
  full of happiness and life; that is the end thereof. That caterpillar is
  yourself, until you are wrapped up in the chrysalis of death, but when Christ
  shall appear you shall be like Him, for you shall see Him as He is. Be content
  to be like Him, a worm and no man, that like Him you may be satisfied when you
  wake up in His likeness. That rough-looking diamond is put upon the wheel of
  the lapidary. He cuts it on all sides, It loses much — much that seemed costly
  to itself. The king is crowned; the diadem is put upon the monarch’s head with
  trumpet’s joyful sound. A glittering ray flashes from that coronet, and it
  beams from that very diamond which was just now so sorely vexed by the
  lapidary. You may venture to compare yourself to such a diamond, for you are
  one of God’s people; and this is the time of the cutting process. Let faith
  and patience have their perfect work, for in the day when the crown shall be
  set upon the head of the King, Eternal, Immortal, Invisible, one ray of glory
  shall stream from you. “They shall be Mine,” saith the Lord, “in the day when
  I make up My jewels.” “Better is the end of a thing than the beginning
  thereof.”
</p>