  <h3>“I am a debtor both to Greeks and to barbarians… –Romans 1:14</h3>
  <hr>
</div>
<p>Paul was overwhelmed with the sense of his indebtedness to Jesus Christ, and
  he spent his life to express it. The greatest inspiration in Paul’s life was
  his view of Jesus Christ as his spiritual creditor Do I feel that same sense
  of indebtedness to Christ regarding every unsaved soul? As a saint, my life’s
  spiritual honor and duty is to fulfill my debt to Christ in relation to these
  lost souls. Every tiny bit of my life that has value I owe to the redemption
  of Jesus Christ. Am I doing anything to enable Him to bringHis redemption into
  evident reality in the lives of others? I will only be able to do this as the
  Spirit of God works into me this sense of indebtedness.</p>
<p>I am not a superior person among other people—I am a bondservant of the Lord
  Jesus. Paul said, “…you are not your own—you were bought at a price…”</p>
<p>1 Corinthians 6:19-20. Paul sold himself to Jesus Christ and he said, in
  effect, “I am a debtor to everyone on the face of the earth because of the
  gospel of Jesus; I am free only that I may be an absolute bondservant of Him.”
  That is the characteristic of a Christian’s life once this level of spiritual
  honor and duty becomes real Quit praying about yourself and spend your lire
  for the sake of others as the bondservant of Jesus. That is the true meaning
  of being broken bread and poured-out wine in real life.</p>