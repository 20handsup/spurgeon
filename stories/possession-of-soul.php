  <p><strong>“By your patience possess your souls” — Luke 21:19</strong></p>
  <hr />
</div>
<p>
  When a person is born again, there is a period of time when he does not have
  the same vitality in his thinking or reasoning that he perviously had. We must
  learn to express this new life within us, which comes by forming the mind of
  Christ (see Philippians 2:5). Luke 21:19 means that we take possession of our
  souls through patience. but many of us prefer stay at the entrance to the
  Christian life, instead of going on to create and build our should in
  accordance with the new life God has placed within us. We fail because we are
  ignorant of the way God has made us, and we blame things on the devil that are
  actually the result of our own undisciplined natures. Just thin what we could
  be when we are awakened to the truth!
</p>
<p>
  There are certain things in life that we need not pray about—moods, for
  instance. We will never get rid of moodiness by praying, but we will by
  kicking it out of our lives. Moods nearly always are rooted in some physical
  circumstance, not in our true inner self. It is a continual struggle not to
  listen to the moods which arise as a result of our physical condition, but we
  must never submit to them for a second. We have to pick ourselves up by the
  back of the neck and shake ourselves; then we will find that we can do what we
  believed we were unable to do. The problem that most of us are cursed with in
  simply that we <em>won’t.</em> The Christian life is one of spiritual courage
  and determination lived out in our flesh.
</p>
