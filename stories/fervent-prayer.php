  <p><b>Praying in the Holy Ghost
      — Jude 20</b></p>
  <hr>
</div>
<p>Mark the grand characteristic of true prayer—<em>“In the Holy Ghost.”</em>
  The seed of acceptable devotion must come from heaven’s storehouse. Only the
  prayer which comes from God can go to God. We must shoot the Lord’s arrows
  back to Him. That desire which He writes upon our heart will move His heart
  and bring down a blessing, but the desires of the flesh have no power with
  Him.</p>
<p>Praying in the Holy Ghost is praying in <em>fervency.</em> Cold prayers ask
  the Lord not to hear them. Those who do not plead with fervency, plead not at
  all As well speak of lukewarm fire as of lukewarm prayer — it is essential
  that it be red hot. It is praying <em>perseveringly</em>. This true suppliant
  gathers force as he proceeds, and grows more fervent when God delays to
  answer. The longer the gate is closed, the more vehemently does he use the
  knocker, and the longer the angel lingers the more resolved is he that he will
  never let him go without the blessing. Beautiful in God’s sight is tearful,
  agonizing, unconquerable importunity. It means praying <em>humbly</em> for the
  Holy Spirit never puffs us up with pride. It is His office to convince of sin,
  and so to bow us down in contrition and brokenness of spirit. We shall never
  sing <em>Gloria in excelsis</em> except we pray to God <em>De profundis:</em>
  out of the depths must we cry, or we shall never behold glory in the highest.
  It is <em>loving</em> prayer. Prayer should be perfumed with love, saturated
  with love — love to our fell saints, and love to Christ. Moreover, it must be
  a prayer full of <em>faith</em>. A man prevails only as he believes. The Holy
  Spirit is the author of faith, and strengthens it, so that we pray believing
  God’s promise. O that this blessed combination of excellent might be fragrant
  within us because the Holy Ghost is in our hearts! Most blessed Comforter,
  exert Thy mighty power within us, helping our infirmities in prayer.
</p>
