  <p>"We love Him because He first loved us." &mdash; 1 John 4:19</p>
  <hr>
</div>

<p>
  There is no light in the planet but that which proceeds from the sun; and
  there is no true love tto Jesus in the heart but that which comes from the
  Lord Jesus Himself. From this overflowing fountain of the infinite love of
  God, all our love to God must spring. This must ever be a great and certain
  truth, that we love Him for no other reason than because He first loved us.
  Our love to Him is <em>the fair offspring</em> of His love to us. Cold
  admiration, when studying the works of God, anyone may have, but the warmth of
  love can only be kindled in the heart by God's Spirit. How great the wonder
  that such as we should ever have been brought to love Jesus at all! How
  marvelous that when we had rebelled against Him, He should, by a display of
  such amazing love, seek to draw us back. No! never should wwe have had a grain
  of love towards God unless it had been sown in us by the sweet seed of His
  love to us. Love, the, has for its parent the love of God shed abroad in the
  heart: but after it is thus divinely born, it must be <em>divinely
    nourished.</em> Love is an exotic; it is not a plant which will flourish
  naturally to human soil, it must be watered from above. Love to Jesus is a
  flower of a delicate nature, and if it received no nourishment but that which
  could be drawn from the rock of our hearts it would soon wither. As love comes
  from heaven, so it must freed on heavenly bread. It cannot exist in the
  wilderness unless it be fed by manna from on high. Love must freed on love.
  The very should and life of our love to God is His love to us.
</p>

